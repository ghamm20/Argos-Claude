#!/usr/bin/env node
// smoke-chat-export.mjs
//
// Pure-function smoke for lib/chat-export.ts. Tests:
//   - markdown structure (header, separators, footer)
//   - speaker labels (User vs persona name)
//   - citation formatting + score precision
//   - empty-content fallback
//   - filename slug + UTC date format
//
// No live server needed. Imports the compiled chat-export module via
// a small TS-runtime shim — we re-implement the logic locally to keep
// the smoke node-runnable without a TS step.

// Re-implementations of the export logic, kept in sync with lib/chat-export.ts.
// If those diverge, this smoke catches the difference.

function chatToMarkdown(messages, ctx) {
  const exportedAt = ctx.exportedAt ?? Date.now();
  const isoTs = new Date(exportedAt).toISOString();
  const personaName = ctx.personaName ?? "ARGOS";
  const lines = [];
  lines.push(`# ARGOS Chat — ${personaName}`);
  lines.push("");
  lines.push(`**Exported:** ${isoTs}  `);
  lines.push(`**Model:** ${ctx.model}  `);
  lines.push(`**Messages:** ${messages.filter((m) => m.role !== "system").length}`);
  lines.push("");
  lines.push("---");
  lines.push("");
  for (const msg of messages) {
    if (msg.role === "system") continue;
    const speaker =
      msg.role === "user"
        ? "User"
        : msg.personaName ?? "Assistant";
    lines.push(`### ${speaker}`);
    lines.push("");
    lines.push(msg.content.trim() || "*(empty)*");
    lines.push("");
    if (msg.retrievalHits && msg.retrievalHits.length > 0) {
      lines.push("**Citations:**");
      lines.push("");
      for (const hit of msg.retrievalHits) {
        const score = hit.score.toFixed(3);
        const preview = hit.text.replace(/\s+/g, " ").trim().slice(0, 240);
        const ellipsis = hit.text.length > 240 ? "…" : "";
        lines.push(`- **[${hit.index}]** \`${hit.filename}\` chunk ${hit.chunkIndex} (score ${score}) — ${preview}${ellipsis}`);
      }
      lines.push("");
    }
  }
  lines.push("---");
  lines.push("");
  lines.push("_Generated by ARGOS — local-first AI workstation._");
  return lines.join("\n");
}

function exportFilename(personaName, exportedAt) {
  const d = new Date(exportedAt);
  const yyyy = d.getUTCFullYear();
  const mm = String(d.getUTCMonth() + 1).padStart(2, "0");
  const dd = String(d.getUTCDate()).padStart(2, "0");
  const hh = String(d.getUTCHours()).padStart(2, "0");
  const mi = String(d.getUTCMinutes()).padStart(2, "0");
  const slug = personaName
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-+|-+$/g, "");
  return `argos-chat-${yyyy}${mm}${dd}-${hh}${mi}-${slug}.md`;
}

let passed = 0;
let failed = 0;
function check(label, cond, detail = "") {
  const tag = cond ? "✓" : "✗";
  console.log(`  ${tag} ${label}${detail ? "  " + detail : ""}`);
  if (cond) passed++;
  else failed++;
}

// ---------- T1: empty chat ----------
console.log("\n[T1] empty messages");
{
  const md = chatToMarkdown([], {
    model: "llama3.1:8b-instruct-q4_K_M",
    personaName: "Bartimaeus",
    exportedAt: 1779200000000,
  });
  check("has H1 header", md.includes("# ARGOS Chat — Bartimaeus"));
  check("has model line", md.includes("**Model:** llama3.1:8b-instruct-q4_K_M"));
  check("messages count = 0", md.includes("**Messages:** 0"));
  check("has footer", md.includes("Generated by ARGOS"));
}

// ---------- T2: basic user + assistant ----------
console.log("\n[T2] simple user + assistant");
{
  const messages = [
    {
      role: "user",
      content: "What is rule 1?",
      timestamp: 1779200000000,
    },
    {
      role: "assistant",
      content: "Rule 1 is zero host persistence.",
      personaName: "Bartimaeus",
      timestamp: 1779200001000,
    },
  ];
  const md = chatToMarkdown(messages, {
    model: "llama3.1:8b",
    personaName: "Bartimaeus",
    exportedAt: 1779200002000,
  });
  check("user speaker label", md.includes("### User"));
  check("persona speaker label", md.includes("### Bartimaeus"));
  check("user content present", md.includes("What is rule 1?"));
  check("assistant content present", md.includes("Rule 1 is zero host persistence."));
  check("messages count = 2", md.includes("**Messages:** 2"));
}

// ---------- T3: system messages are skipped ----------
console.log("\n[T3] system messages skipped");
{
  const messages = [
    { role: "system", content: "You are Bartimaeus.", timestamp: 0 },
    { role: "user", content: "hi", timestamp: 1 },
  ];
  const md = chatToMarkdown(messages, { model: "x", personaName: "B", exportedAt: 0 });
  check("system content NOT in output", !md.includes("You are Bartimaeus"));
  check("messages count = 1 (user only)", md.includes("**Messages:** 1"));
}

// ---------- T4: citations rendered ----------
console.log("\n[T4] citations");
{
  const messages = [
    {
      role: "assistant",
      content: "Per Rule 3 [1], paths are relative.",
      personaName: "Bartimaeus",
      retrievalHits: [
        {
          index: 1,
          text: "Rule 3 — Relative paths only.",
          filename: "seven-rules.md",
          chunkIndex: 2,
          score: 0.598123,
          docId: "abc",
        },
      ],
      timestamp: 0,
    },
  ];
  const md = chatToMarkdown(messages, { model: "x", personaName: "B", exportedAt: 0 });
  check("has citations heading", md.includes("**Citations:**"));
  check("citation index", md.includes("[1]"));
  check("citation filename", md.includes("`seven-rules.md`"));
  check("citation chunk index", md.includes("chunk 2"));
  check("citation score precision", md.includes("score 0.598"));
  check("score precision (3 digits)", !md.includes("score 0.5981") && !md.includes("score 0.59812"));
  check("citation text preview", md.includes("Rule 3 — Relative paths only."));
}

// ---------- T5: long citation truncation ----------
console.log("\n[T5] long citation gets truncated");
{
  const longText = "x".repeat(500);
  const messages = [
    {
      role: "assistant",
      content: "[1]",
      personaName: "Bartimaeus",
      retrievalHits: [
        {
          index: 1,
          text: longText,
          filename: "a.md",
          chunkIndex: 0,
          score: 0.5,
          docId: "a",
        },
      ],
      timestamp: 0,
    },
  ];
  const md = chatToMarkdown(messages, { model: "x", personaName: "B", exportedAt: 0 });
  check("truncation ellipsis present", md.includes("…"));
  // The truncated preview should be ≤ 241 chars (240 + …) — the full 500
  // shouldn't survive verbatim.
  const idx = md.indexOf("xxx");
  check("did not include full 500 chars", !md.includes("x".repeat(300)));
}

// ---------- T6: empty assistant content fallback ----------
console.log("\n[T6] empty content fallback");
{
  const messages = [
    {
      role: "assistant",
      content: "",
      personaName: "Bartimaeus",
      timestamp: 0,
    },
  ];
  const md = chatToMarkdown(messages, { model: "x", personaName: "B", exportedAt: 0 });
  check("renders *(empty)*", md.includes("*(empty)*"));
}

// ---------- T7: filename ----------
console.log("\n[T7] export filename");
{
  // 2026-05-21T18:30:00Z
  const t = Date.UTC(2026, 4, 21, 18, 30, 0);
  const fn = exportFilename("Bartimaeus", t);
  check("starts with argos-chat-", fn.startsWith("argos-chat-"));
  check("contains YYYYMMDD", fn.includes("20260521"));
  check("contains HHMM", fn.includes("-1830-"));
  check("contains persona slug", fn.endsWith("-bartimaeus.md"));
}
{
  const fn = exportFilename("Captain O'Brien!", 0);
  check("special chars become hyphen", fn.includes("captain-o-brien"));
  check("no double hyphens or leading-trailing", !/--+|-\.md$/.test(fn));
}

// ---------- summary ----------
console.log("\n" + "=".repeat(64));
console.log(`smoke-chat-export: ${passed} PASS, ${failed} FAIL`);
process.exit(failed === 0 ? 0 : 1);
